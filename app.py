@app.route("/trades")
def trades():
    if not session.get("user_id"):
        return redirect(url_for("login"))

    username = session.get("username", "User")
    user_id = session["user_id"]
    try:
        user_trades = (
            Trade.query.filter_by(user_id=user_id)
            .order_by(Trade.opened_at.desc())
            .all()
        )
    except OperationalError:
        db.session.rollback()
        user_trades = []

    trade_rows = []
    for trade in user_trades:
        trade_rows.append(
            {
                "id": trade.id,
                "symbol": trade.symbol,
                "side": trade.side,
                "entry_price": trade.entry_price,
                "exit_price": trade.exit_price,
                "lot_size": trade.lot_size,
                "pnl": resolve_pnl(trade),
                "pips": resolve_pips(trade),
                "opened_at": trade.opened_at,
            }
        )

    batch_delete_status = request.args.get("batch_delete_status", "").strip().lower()
    batch_delete_message = request.args.get("batch_delete_message", "").strip()
    if batch_delete_status not in {"success", "error", "info"}:
        batch_delete_status = ""
    if not batch_delete_message:
        batch_delete_status = ""

    import_batch_map = {}
    for trade in user_trades:
        signature = (trade.import_signature or "").strip()
        if not signature:
            continue
        if signature not in import_batch_map:
            imported_at = parse_import_signature_datetime(signature)
            import_batch_map[signature] = {
                "signature": signature,
                "trade_count": 0,
                "imported_at": imported_at,
                "imported_at_label": (
                    imported_at.strftime("%Y-%m-%d %H:%M UTC")
                    if imported_at
                    else "Unknown import time"
                ),
            }
        import_batch_map[signature]["trade_count"] += 1

    import_batches = sorted(
        import_batch_map.values(),
        key=lambda batch: batch["imported_at"] or datetime.min,
        reverse=True,
    )

    return render_template(
        "trades.html",
        title="My Trades | FX Journal",
        username=username,
        trades=trade_rows,
        import_batches=import_batches,
        batch_delete_status=batch_delete_status,
        batch_delete_message=batch_delete_message,
    )

