@app.route("/import", methods=["POST"])
def import_mt5_trades():
    if not session.get("user_id"):
        return redirect(url_for("login"))

    uploaded_file = request.files.get("mt5_file")
    if not uploaded_file or not uploaded_file.filename:
        return redirect(
            url_for(
                "new_trade",
                import_status="error",
                import_message="Please choose an MT5 .xlsx file to upload.",
            )
        )

    filename = uploaded_file.filename.strip().lower()
    if not filename.endswith(".xlsx"):
        return redirect(
            url_for(
                "new_trade",
                import_status="error",
                import_message="Invalid file type. Please upload an .xlsx file.",
            )
        )

    try:
        uploaded_file.stream.seek(0)
        parsed_rows, total_rows, skipped_rows = parse_mt5_xlsx_stream(uploaded_file.stream)
        if not parsed_rows:
            return redirect(
                url_for(
                    "new_trade",
                    import_status="error",
                    import_message="No valid trade rows found in this MT5 file.",
                )
            )

        user_id = session["user_id"]
        allowed_symbols = set(BASE_SYMBOL_OPTIONS)
        failed_symbols = set()
        validation_skipped = 0
        validation_reasons = {
            "missing_mt5_position": 0,
            "invalid_mt5_position": 0,
            "invalid_side": 0,
            "invalid_entry_or_lot": 0,
            "invalid_exit_price": 0,
            "missing_open_time": 0,
        }

        existing_positions = {
            pos
            for (pos,) in db.session.query(Trade.mt5_position)
            .filter_by(user_id=user_id)
            .filter(Trade.mt5_position.isnot(None))
            .all()
            if pos
        }

        import_positions = set()
        import_signature = build_import_signature("mt5")
        duplicate_count = 0
        insert_batch = []

        for row in parsed_rows:
            symbol = canonicalize_symbol(row.get("symbol"))
            mt5_position = row.get("mt5_position")
            mt5_position_raw = row.get("mt5_position_raw")
            side = (row.get("side") or "").upper()
            lot_size = row.get("lot_size")
            entry_price = row.get("entry_price")
            exit_price = row.get("exit_price")
            pnl = row.get("pnl")
            opened_at = row.get("opened_at")

            if symbol not in allowed_symbols:
                if symbol:
                    failed_symbols.add(symbol)
                validation_skipped += 1
                continue

            if mt5_position is None:
                validation_skipped += 1
                raw_text = str(mt5_position_raw or "").strip()
                if raw_text:
                    validation_reasons["invalid_mt5_position"] += 1
                else:
                    validation_reasons["missing_mt5_position"] += 1
                continue

            if side not in {"BUY", "SELL"}:
                validation_skipped += 1
                validation_reasons["invalid_side"] += 1
                continue

            if lot_size is None or lot_size <= 0 or entry_price is None or entry_price <= 0:
                validation_skipped += 1
                validation_reasons["invalid_entry_or_lot"] += 1
                continue

            if exit_price is not None and exit_price <= 0:
                validation_skipped += 1
                validation_reasons["invalid_exit_price"] += 1
                continue

            if opened_at is None:
                validation_skipped += 1
                validation_reasons["missing_open_time"] += 1
                continue

            if pnl is None and exit_price is not None:
                pnl = calc_pnl_values(symbol, side, entry_price, exit_price, lot_size)

            if mt5_position in existing_positions or mt5_position in import_positions:
                duplicate_count += 1
                continue

            import_positions.add(mt5_position)
            insert_batch.append(
                Trade(
                    user_id=user_id,
                    symbol=symbol,
                    mt5_position=mt5_position,
                    import_signature=import_signature,
                    side=side,
                    entry_price=float(entry_price),
                    exit_price=float(exit_price) if exit_price is not None else None,
                    lot_size=float(lot_size),
                    pnl=float(pnl) if pnl is not None else None,
                    opened_at=opened_at,
                    trade_note="Imported from MT5 Positions",
                )
            )

        if not insert_batch:
            symbol_msg = ""
            if failed_symbols:
                failed_list = sorted(failed_symbols)
                shown = ", ".join(failed_list[:8])
                extra = len(failed_list) - 8
                if extra > 0:
                    shown = f"{shown}, +{extra} more"
                symbol_msg = f" Unrecognized symbols: {shown}."
            return redirect(
                url_for(
                    "new_trade",
                    import_status="error",
                    import_message=(
                        f"No trades imported. Parsed {len(parsed_rows)} rows, "
                        f"skipped {skipped_rows + validation_skipped + duplicate_count}."
                        f"{symbol_msg}"
                    ),
                )
            )

        db.session.add_all(insert_batch)
        db.session.commit()

        status = "success"
        if failed_symbols:
            status = "error"
        elif skipped_rows or validation_skipped or duplicate_count:
            status = "info"

        symbol_msg = ""
        if failed_symbols:
            failed_list = sorted(failed_symbols)
            shown = ", ".join(failed_list[:8])
            extra = len(failed_list) - 8
            if extra > 0:
                shown = f"{shown}, +{extra} more"
            symbol_msg = f" Unrecognized symbols skipped: {shown}."

        uncertain_bits = []
        if validation_reasons["missing_mt5_position"]:
            uncertain_bits.append(
                f"missing MT5 position {validation_reasons['missing_mt5_position']}"
            )
        if validation_reasons["invalid_mt5_position"]:
            uncertain_bits.append(
                f"invalid MT5 position {validation_reasons['invalid_mt5_position']}"
            )
        if validation_reasons["invalid_side"]:
            uncertain_bits.append(f"invalid side {validation_reasons['invalid_side']}")
        if validation_reasons["invalid_entry_or_lot"]:
            uncertain_bits.append(f"invalid entry/lot {validation_reasons['invalid_entry_or_lot']}")
        if validation_reasons["invalid_exit_price"]:
            uncertain_bits.append(f"invalid exit price {validation_reasons['invalid_exit_price']}")
        if validation_reasons["missing_open_time"]:
            uncertain_bits.append(f"missing open time {validation_reasons['missing_open_time']}")
        uncertain_msg = ""
        if uncertain_bits:
            uncertain_msg = " Data uncertainty checks: " + ", ".join(uncertain_bits) + "."

        return redirect(
            url_for(
                "new_trade",
                import_status=status,
                import_message=(
                    f"Imported {len(insert_batch)} trades from Positions. "
                    f"Parsed rows: {len(parsed_rows)}/{total_rows}. "
                    f"Skipped: {skipped_rows + validation_skipped}. "
                    f"Duplicate positions: {duplicate_count}."
                    f" Import signature: {import_signature}."
                    f"{symbol_msg}"
                    f"{uncertain_msg}"
                ),
            )
        )
    except Exception:
        db.session.rollback()
        return redirect(
            url_for(
                "new_trade",
                import_status="error",
                import_message="Upload processing failed. Check MT5 export format and try again.",
            )
        )


